package com.team6.cinema.coupon;


/***
 * 쿠폰 보유 DTO 클래스
 * @author 6조
 *
 */
public class CouponHaveDTO {
	
	private String couponHaveSeq;
	private String userSeq;
	private String couponSeq;
	
	
	
	public String getCouponHaveSeq() {
		return couponHaveSeq;
	}
	public void setCouponHaveSeq(String couponHaveSeq) {
		this.couponHaveSeq = couponHaveSeq;
	}
	public String getUserSeq() {
		return userSeq;
	}
	public void setUserSeq(String userSeq) {
		this.userSeq = userSeq;
	}
	public String getCouponSeq() {
		return couponSeq;
	}
	public void setCouponSeq(String couponSeq) {
		this.couponSeq = couponSeq;
	}
	
	
	

}
